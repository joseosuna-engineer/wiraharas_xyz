package xyz.wiraharas.dto;

public class CuentaDto {

    private String rut;
    private String number;

    public CuentaDto() {
        super();
    }

    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

}
